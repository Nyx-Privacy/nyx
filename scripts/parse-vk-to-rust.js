#!/usr/bin/env node
/**
 * Convert a snarkjs Groth16 verification_key.json into a Rust constant module
 * consumable by `groth16-solana`.
 *
 * The `groth16-solana` library expects the verifying key as:
 *   - vk_alpha_g1: [u8; 64]              (G1 point, big-endian affine x||y)
 *   - vk_beta_g2:  [u8; 128]             (G2 point, big-endian, x = (c1||c0), y = (c1||c0))
 *   - vk_gamma_g2: [u8; 128]
 *   - vk_delta_g2: [u8; 128]
 *   - vk_ic: Vec<[u8; 64]>               (one IC per public input + 1)
 *
 * snarkjs emits the G2 coordinates in [c0, c1] order — for groth16-solana we
 * must swap to [c1, c0]. All field elements are encoded big-endian, 32 bytes.
 *
 * Usage:
 *   node scripts/parse-vk-to-rust.js \
 *     circuits/build/valid_spend/verification_key.json \
 *     programs/vault/src/zk/vk_valid_spend.rs \
 *     VALID_SPEND
 */

"use strict";

const fs = require("fs");
const path = require("path");

function bnToBytesBE(decStr) {
  // Parse decimal string to 32-byte big-endian.
  let n = BigInt(decStr);
  const out = new Uint8Array(32);
  for (let i = 31; i >= 0; i--) {
    out[i] = Number(n & 0xffn);
    n >>= 8n;
  }
  if (n !== 0n) {
    throw new Error(`value ${decStr} does not fit in 32 bytes`);
  }
  return out;
}

function bytesToRustArray(bytes) {
  const items = [];
  for (const b of bytes) items.push(b);
  return `[${items.map((x) => x.toString()).join(", ")}]`;
}

function g1ToBytes(point) {
  const x = bnToBytesBE(point[0]);
  const y = bnToBytesBE(point[1]);
  const out = new Uint8Array(64);
  out.set(x, 0);
  out.set(y, 32);
  return out;
}

function g2ToBytes(point) {
  // snarkjs: [[x0, x1], [y0, y1]]
  // groth16-solana expects: [x1, x0, y1, y0] each 32 BE bytes -> 128 total.
  const x0 = bnToBytesBE(point[0][0]);
  const x1 = bnToBytesBE(point[0][1]);
  const y0 = bnToBytesBE(point[1][0]);
  const y1 = bnToBytesBE(point[1][1]);
  const out = new Uint8Array(128);
  out.set(x1, 0);
  out.set(x0, 32);
  out.set(y1, 64);
  out.set(y0, 96);
  return out;
}

function main() {
  const [, , inPath, outPath, constPrefix] = process.argv;
  if (!inPath || !outPath || !constPrefix) {
    console.error("usage: parse-vk-to-rust.js <vk.json> <out.rs> <CONST_PREFIX>");
    process.exit(1);
  }

  const vk = JSON.parse(fs.readFileSync(inPath, "utf8"));
  if (vk.protocol !== "groth16") throw new Error("not groth16");
  if (vk.curve !== "bn128") throw new Error("not bn128");

  const alpha = g1ToBytes(vk.vk_alpha_1);
  const beta = g2ToBytes(vk.vk_beta_2);
  const gamma = g2ToBytes(vk.vk_gamma_2);
  const delta = g2ToBytes(vk.vk_delta_2);
  const ic = vk.IC.map((p) => g1ToBytes(p));

  const lines = [];
  lines.push(`// AUTO-GENERATED by scripts/parse-vk-to-rust.js`);
  lines.push(`// Source: ${path.basename(inPath)}`);
  lines.push(`// Public inputs: ${vk.nPublic}`);
  lines.push(`// Do not edit by hand.`);
  lines.push(``);
  lines.push(`#![allow(dead_code)]`);
  lines.push(``);
  lines.push(`pub const ${constPrefix}_NR_PUBLIC_INPUTS: usize = ${vk.nPublic};`);
  lines.push(``);
  lines.push(`pub const ${constPrefix}_ALPHA_G1: [u8; 64] = ${bytesToRustArray(alpha)};`);
  lines.push(``);
  lines.push(`pub const ${constPrefix}_BETA_G2: [u8; 128] = ${bytesToRustArray(beta)};`);
  lines.push(``);
  lines.push(`pub const ${constPrefix}_GAMMA_G2: [u8; 128] = ${bytesToRustArray(gamma)};`);
  lines.push(``);
  lines.push(`pub const ${constPrefix}_DELTA_G2: [u8; 128] = ${bytesToRustArray(delta)};`);
  lines.push(``);
  lines.push(`pub const ${constPrefix}_IC: [[u8; 64]; ${ic.length}] = [`);
  for (const b of ic) {
    lines.push(`    ${bytesToRustArray(b)},`);
  }
  lines.push(`];`);
  lines.push(``);

  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, lines.join("\n"));
  console.log(`[parse-vk-to-rust] wrote ${outPath}`);
}

main();
